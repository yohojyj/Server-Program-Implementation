package nado.vo;

public class Follow {
	protected int from_user;
	protected int to_user;
	
	
	public int getFrom_user() {
		return from_user;
	}
	public Follow setFrom_user(int from_user) {
		this.from_user = from_user;
		return this;
	}
	
	
	public int getTo_user() {
		return to_user;
	}
	public Follow setTo_user(int to_user) {
		this.to_user = to_user;
		return this;
	}
	
	

}
